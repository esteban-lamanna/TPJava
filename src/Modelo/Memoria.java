package Modelo;

public class Memoria extends Producto
{
	float frecuencia;
	String latencia;
	int cantmen;
	
	public float getFrecuencia() {
		return frecuencia;
	}
	public void setFrecuencia(float frecuencia) {
		this.frecuencia = frecuencia;
	}
	public String getLatencia() {
		return latencia;
	}
	public void setLatencia(String latencia) {
		this.latencia = latencia;
	}
	public int getCantmen() {
		return cantmen;
	}
	public void setCantmen(int cantmen) {
		this.cantmen = cantmen;
	}

	
}
